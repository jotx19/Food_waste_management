package Models;

public class Passwordvalidator {
    private static final String PASSWORD_PATTERN = 
        "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&])[A-Za-z\\d@$!%*?&]{8,}$";

    public static boolean validate(String password) {
        return password != null && password.matches(PASSWORD_PATTERN);
    }
}
